    /**
     * Lookup DNO location from postcode district.
     *
     * Distribution Network Operators (DNO) in the UK.
     * ------------------------------------------------
     * 10 | East England            | Eastern Electric
     * 11 | East Midlands           | East Midlands Electricity
     * 12 | London                  | London Electricity
     * 13 | North Wales / Cheshire  | Manweb
     * 14 | West Midlands           | Midlands Electricity
     * 15 | North East England      | Northern Electric
     * 16 | North West England      | Norweb
     * 17 | North Scotland          | Scottish Hydro Electric
     * 18 | South Scotland          | Scottish Power
     * 19 | South East England      | SEEBOARD
     * 20 | Southern England        | Southern Electric
     * 21 | South Wales             | South Wales Electricity
     * 22 | South West England      | South Western Electricity
     * 23 | Yorkshire               | Yorkshire Electricity
     *
     * @param {string} postcodeDistrict A postcode district.
     * @returns {boolean|string}
     */
    let postcodeDistrictToDNOLocation = (postcodeDistrict) => {
        // Uppercase for matching.
        postcodeDistrict = postcodeDistrict.toUpperCase();

        let output = lookup[postcodeDistrict];

        return typeof output === "undefined" || output === null ? false : output;
    };

    return {
        'postcodeDistrictToDNOLocation' : postcodeDistrictToDNOLocation
    }
